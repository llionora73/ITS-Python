# Memorizzare un numero in virgola mobile nella variabile x
x = 3.14159  


# Calcolare 1.0 / x e memorizzare il risultato nella variabile y
y = 1.0 / x

# Visualizzare il valore di x, y e il prodotto tra x e y
print(f"Valore di x: {x}")
print(f"Valore di y (1.0 / x): {y}")
print(f"Prodotto tra x e y: {x * y}")

# Sottrarre x dal prodotto tra x e y e mostrarne il risultato
result = (x * y) - x
print(f"Risultato della sottrazione (x * y) - x: {result}")